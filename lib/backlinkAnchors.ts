/**
 * Get anchor texts by category
 */
export function getAnchorsByCategory(category: 'location' | 'model' | 'price' | 'financing' | 'dealer' | 'quality'): string[] {
  const categoryMap: { [key: string]: string[] } = {
    location: backlinkAnchors.filter(anchor => 
      anchor.includes('Milwaukee') || anchor.includes('Wisconsin') || anchor.includes('near')
    ),
    model: backlinkAnchors.filter(anchor => 
      anchor.includes('Street Glide') || anchor.includes('Road Glide') || 
      anchor.includes('Fat Boy') || anchor.includes('Sportster') || 
      anchor.includes('Softail') || anchor.includes('Heritage') ||
      anchor.includes('Low Rider') || anchor.includes('Road King')
    ),
    price: backlinkAnchors.filter(anchor => 
      anchor.includes('price') || anchor.includes('cheap') || 
      anchor.includes('affordable') || anchor.includes('discount') ||
      anchor.includes('deal') || anchor.includes('budget')
    ),
    financing: backlinkAnchors.filter(anchor => 
      anchor.includes('financing') || anchor.includes('loan') || 
      anchor.includes('payment') || anchor.includes('credit')
    ),
    dealer: backlinkAnchors.filter(anchor => 
      anchor.includes('dealer') || anchor.includes('dealership') || 
      anchor.includes('shop') || anchor.includes('service')
    ),
    quality: backlinkAnchors.filter(anchor => 
      anchor.includes('mileage') || anchor.includes('inspected') || 
      anchor.includes('certified') || anchor.includes('condition') ||
      anchor.includes('maintained') || anchor.includes('quality')
    ),
  };
  
  return categoryMap[category] || [];
}

